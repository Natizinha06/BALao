public class Pessoa{

        public String nome;
        public String Cpf;
        public String RG;
        public double Telefone;
        public double telContado;
        public String endereço;
        public String assinatura;


        public Pessoa(String nome, String Cpf,String RG,double Telefone,double telContado,String endereço,String assinatura){
            this.nome = nome;
            this.RG = RG;
            this.Cpf = Cpf;
            this.Telefone = Telefone;
            this.telContado = telContado;
            this.endereço =endereço;
            this.assinatura = assinatura;

        }
}
